    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String consulta=(String)jTable1.getValueAt((int)jTable1.getSelectedRow(),0);
        int confirmacion;
        boolean banner=false;
        String tabla="",dato="";
        if(opcion==0){
            tabla="zapateria.zapato";
            dato="codigo";
        }else if(opcion==1){
            tabla="zapateria.empleado";
            dato="id_e";
        }else if(opcion==2){
            tabla="zapateria.proveedor";
            dato="id_p";
        }
        try{
            ResultSet r=sentencia.executeQuery("SELECT "+dato+" FROM "+tabla+";");
            while(r.next()){
                if(consulta.equalsIgnoreCase(r.getString(dato))){
                    banner=true;
                }
            }
            if(banner==true){
                confirmacion=JOptionPane.showConfirmDialog(null,"Quiere borrar?","confirmación",JOptionPane.YES_NO_OPTION);
                if(confirmacion==JOptionPane.YES_OPTION){
                    sentencia.executeUpdate("DELETE FROM "+tabla+" WHERE "+dato+" ="+consulta+";");
                    if(opcion==0){
                        llenarTabla1();    
                    }else if(opcion==1){
                        llenarTabla2();
                    }else if(opcion==2){
                        llenarTabla3();
                    }
                }
            }else{
                JOptionPane.showMessageDialog(null,"No se ah encontrado");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error al consultar tabla");
            System.out.println(e);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

