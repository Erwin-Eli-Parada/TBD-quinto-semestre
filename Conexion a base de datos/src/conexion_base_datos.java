    public void PrepararBaseDatos(){
        try{
            String controlador="org.postgresql.Driver";
            Class.forName(controlador).newInstance();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error al cargar el controlador");
        }
        try{
            String DNS= "jdbc:postgresql://localhost:5432/TALLERBD";
            String user=JOptionPane.showInputDialog("ingrese el usuario");
            String password=JOptionPane.showInputDialog("ingrese la contraseña");
            conexion=DriverManager.getConnection(DNS,user,password);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error al realizar la conexión");
        }
        try{
            sentencia=conexion.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,ResultSet.CONCUR_READ_ONLY);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error al crear sentencia");
        }
    }
        
    public void llenarTabla1(){
        numero_clientes=0;
        String info="";
        String [] titulos={"codigo","descripción","proveedor","talla","color","material","precio_c","precio_v","existencia"};
        m=new DefaultTableModel(null,titulos);
        jTable1.setModel(m);
        try{
            ResultSet r=sentencia.executeQuery("SELECT * FROM zapateria.zapato;");
            while(r.next()){
                m=(DefaultTableModel) jTable1.getModel();
                String[] filaElemento={r.getString("codigo"),r.getString("descripcion"),r.getString("prov"),r.getString("talla"),r.getString("color"),r.getString("material"),r.getString("precio_c"),r.getString("precio_v"),r.getString("existencia")};
                m.addRow(filaElemento);
                numero_clientes++;
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error al crear consultar tabla");
        }
    }
